def format_subscription_info(subscriptions):
    """–§–æ—Ä–º–∞—Ç–∏—Ä–æ–≤–∞–Ω–∏–µ –∏–Ω—Ñ–æ—Ä–º–∞—Ü–∏–∏ –æ –ø–æ–¥–ø–∏—Å–∫–∞—Ö –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è –¥–ª—è –≤—ã–≤–æ–¥–∞."""
    if not subscriptions:
        return "–£ –≤–∞—Å –Ω–µ—Ç –∞–∫—Ç–∏–≤–Ω—ã—Ö –ø–æ–¥–ø–∏—Å–æ–∫."
    subscription_lines = []
    for sub in subscriptions:
        line = f"{sub['name']}: {sub['status']} –¥–æ {sub['expires']}"
        subscription_lines.append(line)
    return "\n".join(subscription_lines)

